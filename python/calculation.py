num1 = int(input("Enter a number :: "))  # taking first input from user
num2 = int(input("Enter a number :: "))  # taking second input from user

# addition
addition = num1+num2
print("addition of two number is ::", addition)

# substraction
subsctration = num1-num2
print("substraction of two number is ::", subsctration)

# multiplication
multiplication = num1*num2
print("multiplication of two number is ::", multiplication)

# division
division = num1/num2    # for Int division we can use //
print("division of two number is ::", division)
